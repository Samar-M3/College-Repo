-- 10% SALARY RAISE FOR RESEARCH DEPARTMENT
SELECT E.FNAME, E.LNAME,
       E.SALARY * 1.1 AS INCREASED_SALARY
FROM employee E
JOIN department D ON E.DNO = D.DNUMBER
WHERE D.DNAME = 'Research';

-- SALARY STATISTICS FOR HR DEPARTMENT
SELECT
    SUM(E.SALARY) AS TOTAL,
    MAX(E.SALARY) AS MAX,
    MIN(E.SALARY) AS MIN,
    AVG(E.SALARY) AS AVERAGE
FROM employee E
JOIN department D ON E.DNO = D.DNUMBER
WHERE D.DNAME = 'HR';

-- EMPLOYEES CONTROLLED BY DEPARTMENT NO 5
SELECT FNAME, LNAME FROM employee E
WHERE EXISTS (
    SELECT * FROM employee
    WHERE DNO = 5
);

-- DEPARTMENTS HAVING AT LEAST 2 EMPLOYEES
SELECT D.DNAME, COUNT(*) AS EMP_COUNT
FROM employee E
JOIN department D ON E.DNO = D.DNUMBER
GROUP BY D.DNUMBER, D.DNAME
HAVING COUNT(*) >= 2;

