-- CREATE DEPARTMENT TABLE
CREATE TABLE department (
    DNAME       VARCHAR2(50),
    DNUMBER     NUMBER PRIMARY KEY,
    MGRSSN      VARCHAR2(15),
    MGRSTARTDATE DATE
);

-- CREATE EMPLOYEE TABLE
CREATE TABLE employee (
    FNAME     VARCHAR2(255),
    MINIT     CHAR(1),
    LNAME     VARCHAR2(20),
    SSN       VARCHAR2(15) PRIMARY KEY,
    BDATE     DATE,
    ADDRESS   VARCHAR2(30),
    SEX       CHAR(1),
    SALARY    NUMBER,
    SUPERSSON VARCHAR2(15),
    DNO       NUMBER,
    CONSTRAINT fk_dno FOREIGN KEY (DNO)
    REFERENCES department(DNUMBER)
);

